/**
 * Writes env.local.js from GOOGLE_API_KEY (same value as GitHub secret GOOGLE_API_KEY).
 * Run:  PowerShell: $env:GOOGLE_API_KEY="..." ; npm run env:local
 */
const fs = require('fs');
const path = require('path');

const key = process.env.GOOGLE_API_KEY;
if (!key) {
  console.error('GOOGLE_API_KEY is not set.');
  console.error('Use the same API key as in GitHub → repo → Settings → Secrets → GOOGLE_API_KEY.');
  console.error('PowerShell: $env:GOOGLE_API_KEY="AIza..." ; npm run env:local');
  process.exit(1);
}

const out = path.join(__dirname, '..', 'env.local.js');
const body = `// Generated by npm run env:local — listed in .gitignore, do not commit\nwindow.__API_KEY__ = ${JSON.stringify(key)};\n`;
fs.writeFileSync(out, body, 'utf8');
console.log('OK: wrote', out);
